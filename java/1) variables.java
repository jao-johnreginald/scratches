class Scratch {



    public static void main(String[] args) {

        // This 'String' variable is for words, text
        String barneyString = "barney is a dinosaur";

        // This 'integer' variable is for whole numbers without decimals
        int barneyNumber = 69;

        // This 'double' variable is for decimal numbers
        double barneyDecimal = 69.6;

        //This 'boolean' variable is for stating true or false
        boolean barneyTrueOrFalse = true;

        // System.out.println() is to log, or 'print' the values, to check them
        System.out.println(barneyString);
        System.out.println(barneyNumber);
        System.out.println(barneyDecimal);
        System.out.println(barneyTrueOrFalse);

        // we can do math with our variables
        int a = 35;
        int b = 34;

        int result = a + b;

        System.out.println(a + " + " + b + " = " + result);

        // we can create a variable, then change it later
        String barneyText = "barney wants to take a piss";
        System.out.println(barneyText);
        barneyText = "but he later changed his mind";
        System.out.println(barneyText);

    }



}